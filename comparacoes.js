// == comparação implicita

const numero = 5;
const texto = "5";

console.log(numero == texto);

// return true -> mesmo sendo string

// === comparação explicita

const numero1 = 5;
const texto1 = "5";

console.log(numero1 === texto1);

// para identificar os tipos de dados que a variavel esta armazenando

console.log(typeof numero1);
console.log(typeof texto1);

// == só compara o valor
// === compara o valor e o tipo de dado

// so usar os === como boas praticas

